package com.bptn.course._02_operators._boolean;

public class Boolean {

	public static void main(String[] args) {


		int a = 5;
		int b = 2;
		
		boolean result;
		
		result = a==b;
		result = a!=b;
		result = a>b;
		result = a<b;
		result = a>=b;
		result = a<=b;
		
	System.out.println("Is a equal to b? " + result);
	System.out.println("Is a not equal to b? " + result);
	System.out.println("Is a great than b? " + result);
	System.out.println("Is a less than b? " + result);
	System.out.println("Is a greater or equal to b? " + result);
	System.out.println("Is a less or equal to b? " + result);
	

	}

}
